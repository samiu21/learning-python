number = 223232323          # when it is number no need Quotation
name = "samiul sami 21"     # when it is string with number you need quotation
name2 = 'HSTU'               # we can use single quotation for string
print(number)             # it will print the value of number variable
print(name)               # it will print the value of name variable
print(name2)              # it will print the value of name2 variable
print("number")         # it will print only number because of double quotation
print("name")             # it will print only name because of double quotation
