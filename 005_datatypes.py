# Different types of datatypes

text_type_data = "This is a text data type"
print(text_type_data)

# To know the datatype of the variables we will use the syntax given below

print(type(text_type_data))

# complex datatype  use
a = 2+3j      # Here is real part and imaginary part
b = 4-2j      # Imaginary part will attached with j
print(a+b)    # result is (6+1j)
print(a-b)    # result is (-2+5j)

# using bool data type

x = 33
y = 99
print(x > y)   # result is False
print(x < y)   # result is true

# none data types
# it never gives any value it is a null value

jj = None
print(jj)
print(type(jj))  # it will data type is  NoneType

d = ""
print(d)
print(type(d))  # it will show  datatype is string datatype
e = 0
print(e)
print(type(e))  # it will datatype is  int data type
